#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*p;

	p = (char *)malloc(ft_strlen(str) * sizeof(char) + 1);
	i = 0;
	if (p == NULL)
		return (NULL);
	while (str[i])
	{
		p[i] = str[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
