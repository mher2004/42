#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char	a;

	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
	}
	a = (nb % 10) + '0';
	write(fd, &a, 1);
}
